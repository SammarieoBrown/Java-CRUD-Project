    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        if(VNameEntry.getText().isEmpty() ||
                VSizeEntry.getText().isEmpty() ||
                FoodAreaEntry.getText().isEmpty() ||
                StageAreaEntry.getText().isEmpty() ||
                BarAreaEntry.getText().isEmpty() ||
                NumSecurityEntry.getText().isEmpty() ||
                BasePriceEntry.getText().isEmpty() ||
                LevelEntry.getText().isEmpty() )
        {
            JOptionPane.showMessageDialog(null, "Please Enter All Fields");
        }
        else{
            int selectedIndex = jComboBox1.getSelectedIndex();
            venues.get(selectedIndex).setName(VNameEntry.getText().trim());
            venues.get(selectedIndex).setPrice(Double.parseDouble(BasePriceEntry.getText().trim()));
            venues.get(selectedIndex).setSize(Double.parseDouble(VSizeEntry.getText().trim()));
            venues.get(selectedIndex).setFoodArea(Double.parseDouble(FoodAreaEntry.getText().trim()));
            venues.get(selectedIndex).setBarArea(Double.parseDouble(BarAreaEntry.getText().trim()));
            venues.get(selectedIndex).setStageArea(Double.parseDouble(StageAreaEntry.getText().trim()));
            venues.get(selectedIndex).setLevel(Integer.parseInt(LevelEntry.getText().trim()));
            venues.get(selectedIndex).setNumSecurity(Integer.parseInt(NumSecurityEntry.getText().trim()));



            saveVenueToFile();
        }
    }

