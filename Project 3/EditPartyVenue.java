    public void saveVenueToFile(){
        try{
            FileOutputStream file = new FileOutputStream("PartyVenue.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            for(int i=0; i<venues.size(); i++){
                outputFile.writeObject(venues.get(i));

            }
            outputFile.close();
            JOptionPane.showMessageDialog(null,"File Successfully Save");
            this.dispose();

        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null,e.getMessage());

        }
    }
    public void saveVenueToFileDelete(){
        try{
            FileOutputStream file = new FileOutputStream("PartyVenue.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            for(int i=0; i<venues.size(); i++){
                outputFile.writeObject(venues.get(i));

            }
            outputFile.close();
            JOptionPane.showMessageDialog(null," Successfully Deleted");
            this.dispose();

        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null,e.getMessage());

        }
    }
    public void poplulateArryList(){
        try{
            FileInputStream file = new FileInputStream("PartyVenue.dat");
            ObjectInputStream inputFile = new ObjectInputStream(file);

            boolean endOfFile = false;

            while (!endOfFile){
                try{
                    venues.add((PartyVenue) inputFile.readObject());
                }
                catch(EOFException e){
                    endOfFile = true;

                }
                catch(Exception f){
                    JOptionPane.showMessageDialog(null,f.getMessage());

                }
            }
            inputFile.close();


        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }

    }

