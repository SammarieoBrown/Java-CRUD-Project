    public void poplulateArryList(){
        try{
            FileInputStream file = new FileInputStream("Promoter.dat");
            ObjectInputStream inputFile = new ObjectInputStream(file);
            
            boolean endOfFile = false;
            
            while (!endOfFile){
                try{
                    Promoters.add((Promoter) inputFile.readObject());
                }
                catch(EOFException e){
                    endOfFile = true;
                    
                }
                catch(Exception f){
                    JOptionPane.showMessageDialog(null,f.getMessage());
                    
                }
            }
            inputFile.close();
            
            
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        
    }
    public void savePromoterToFile(){
        try{
            FileOutputStream file = new FileOutputStream("Promoter.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            for(int i=0; i<Promoters.size(); i++){
                outputFile.writeObject(Promoters.get(i));
                
            }
            outputFile.close();
            JOptionPane.showMessageDialog(null,"File Successfully Save");
            this.dispose();
            
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null,e.getMessage());
            
        }
    }

