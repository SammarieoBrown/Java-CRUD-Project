    private void PromoterSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PromoterSaveButtonActionPerformed
        // TODO add your handling code here:
        
        if(PromoterNameEntry.getText().isEmpty() || 
                PromoterAgeEntry.getText().isEmpty() ||
                PromoterBudgetEntry.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Please Enter All Fields");
        }
        else{
            String PromoterName = PromoterNameEntry.getText().trim();
            String PromoterAge = PromoterAgeEntry.getText().trim();
            String PromoterBudget = PromoterBudgetEntry.getText().trim();
            boolean WillPublish;
            WillPublish = checkBox.isSelected();
            
            Promoter Newpromoter = new Promoter(PromoterName,
                    Integer.parseInt(PromoterAge),
                    WillPublish,
                    Double.parseDouble(PromoterBudget));
            
            Promoters.add(Newpromoter);
            savePromoterToFile();
            
            
  
        }
        
        
        
        
    }//GEN-LAST:event_PromoterSaveButtonActionPerformed



